board = hard
count = remaining(board)
rows, columns, options = size(board)
attempts = 0
last = 0
while count > 0:
    if last == count:
        print('giving up!')
        break
    last = count
    attempts += 1
    for y in range(options):
        for x in range(options):
            result, value = evaluate(board, x, y)
            if result == 'valid':
                update(board, x, y, value)
    count = remaining(board)

for y in range(options):
    line = ''
    for x in range(options):
        line = line + str(get(board, x, y))
    print(line)

